package beans;

public class SmartBean {

	private String name;
	
	private MyBean bean;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public MyBean getBean() {
		return bean;
	}

	public void setBean(MyBean bean) {
		this.bean = bean;
	}
	
}
